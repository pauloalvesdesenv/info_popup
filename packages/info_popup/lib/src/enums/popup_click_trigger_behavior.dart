/// [PopupClickTriggerBehavior] is used to specify the trigger behavior of the info popup.
enum PopupClickTriggerBehavior {
  /// [onTap] is used to trigger the popup when the user taps on the child widget.
  onTap,

  /// [onLongPress] is used to trigger the popup when the user long presses on the child widget.
  onLongPress,

  /// [none] is used to disable the trigger behavior of the popup.
  none,
}
